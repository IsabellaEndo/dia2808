package carros;

public class animal {
	
	
	String porte;
	String tipoanimal;
	int numPatas;
	String sexo;
	
	String tipolocomocao;
	String som;
	String amamenta;
	String gera;
	
	public String getporte() {
		return porte;
	}
	public void setporte(String porte) {
		this.porte = porte;
	}
	public String gettipoanimal() {
		return tipoanimal;
	}
	public void settipoanimal(String tipoanimal) {
		this.tipoanimal = tipoanimal;
	}
	public int getnumPatas() {
		return numPatas;
	}
	public void setnumPatas(int numPatas) {
		this.numPatas = numPatas;
	}
	public String getsexo() {
		return sexo;
	}
	public void setsexo(String sexo) {
		this.sexo = sexo;
	}
	public String gettipolocomocao() {
		return tipolocomocao;
	}
	public void settipolocomocao(String tipolocomocao) {
		this.tipolocomocao = tipolocomocao;
	}
	public String getsom() {
		return som;
	}
	public void setsom(String som) {
		this.som = som;
	}
	public String getamamenta() {
		return amamenta;
	}
	public void setamamenta(String amamenta) {
		this.amamenta = amamenta;
	}
	public String getgera() {
		return gera;
	}
	public void setgera(String gera) {
		this.gera = gera;
	}
	

}
