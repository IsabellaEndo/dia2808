package carros;

public class classeCliente {
	
	int id;
	String nome;
	int telefone;
	int CPF;
	int RG;
	
	String encherga;
	String trabalha;
	String anda;
	String fala;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getid() {
		return id;
	}
	public void setId (int id) {
		this.id= id;
	}
	public int gettelefone () {
		return telefone;
	}
	public void settelefone (int telefone) {
		this.telefone = telefone;
	}
	public int getCPF() {
		return CPF;
	}
	public void setCPF (int CPF) {
		this.CPF = CPF;
	}
	public int getRG() {
		return RG;
	}
	public void setRG (int RG) {
		this.RG = RG;
	}
	public String getencherga() {
		return encherga;
	}
	public void setencherga(String encherga) {
		this.encherga = encherga;
	}
	public String gettrabalha() {
		return trabalha;
	}
	public void settrabalha(String trabalha) {
		this.trabalha = trabalha;
	}
	public String getanda() {
		return anda;
	}
	public void setanda(String anda) {
		this.anda = anda;
	}
	public String getfala() {
		return fala;
	}
	public void setfala(String fala) {
		this.fala = fala;
	}


	
}

